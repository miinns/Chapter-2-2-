using System;
class ShiftOperatorApp
{
    public static void Main()
    {
        Console.WriteLine(" 10 << 2 = " + (10 << 2));
        Console.WriteLine(" -9 << 2 = " + ((-9) << 2));
        Console.WriteLine(" 10 >> 2 = " + (10 >> 2));
        Console.WriteLine(" -9 >> 2 = " + ((-9) >> 2));
    }
}